def generate_guidance(result: dict) -> dict:
    concern = result.get("concern_level", "low")

    high_dims = []
    medium_dims = []
    advice_items = []

    for key, label in _DIMENSION_LABELS.items():
        score = result.get(key, 1)
        if score is None:
            score = 1
        if score >= 4:
            high_dims.append((label, score))
            dim_advice = _DIMENSION_ADVICE.get(key, {}).get(score, [])
            for item in dim_advice:
                advice_items.append(f"【{label}】{item}")
        elif score == 3:
            medium_dims.append((label, score))
            dim_advice = _DIMENSION_ADVICE.get(key, {}).get(3, [])
            for item in dim_advice[:2]:
                advice_items.append(f"【{label}】{item}")

    general = _GENERAL_ADVICE.get(concern, _GENERAL_ADVICE["low"])

    guidance = {
        "concern_title": general["title"],
        "concern_message": general["message"],
        "general_actions": general["actions"],
        "specific_advice": advice_items,
        "high_concern_dimensions": high_dims,
        "medium_concern_dimensions": medium_dims,
    }

    if concern == "high":
        guidance["crisis_message"] = _CRISIS_MESSAGE

    return guidance
